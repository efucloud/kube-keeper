export const getIcon = (
  twoLine: boolean,
  category: string,
  height: string,
  onlyIcon: boolean,
) => {
  if (category.startsWith('http://') || category.startsWith('https://')) {
    return <Avatar src={category} />;
  }
  const icons = embedDynamicIcons(twoLine, height, onlyIcon);
  for (let i = 0; i < icons.length; i++) {
    if (category === icons[i].value) {
      return icons[i].label;
    }
  }
  return <Avatar alt={category} />;
};
